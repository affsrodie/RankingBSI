   public void ValidarCampos(int TIPO){
      int Numero=0;
      String Nome="", RG="", CPF="", Nascimento="", Observacao="", TipoSanguineo="", 
      			Telefone="", Rua="", Bairro="", CEP="", PlanoSaude="", DescricaoPS="", DataContratacao="", CRM="", DescricaoEsp="";
      String CamposVazios="";
      
      boolean CamposInvalidos = false;
      
        switch (TIPO) {
            case 1:
                Nome = tfdNomePaciente.getText();
                RG =  tfdRGPaciente.getText();
                CPF = tfdCPFPaciente.getText();
                Nascimento = tfdNascPaciente.getText();
                Observacao = tfdObservacao.getText();
                TipoSanguineo =jcbTipoSanguineos.getSelectedItem().toString();
                Telefone =tfdTelefonePaciente.getText();
                Rua = tfdRuaPaciente.getText();
                Bairro = tfdBairro.getText();
                try {
                    Numero = Integer.parseInt(tfdNumeroPaciente.getText());
                } catch (NumberFormatException nfe) {
                    JOptionPane.showMessageDialog(null, "Formato de Número Inválido");
                }       CEP = tfdCEPPaciente.getText();
                PlanoSaude = tfdPlanoSaude.getText();
                DescricaoPS = tfdDescricaoPS.getText();
                break;
            case 2:
                Nome = tfdNomeAtendente.getText();
                RG =  tfdRGAtendente.getText();
                CPF = tfdCPFAtendente.getText();
                Nascimento = tfdNascimentoAtendente.getText();
                Telefone =tfdTelefoneAtendente.getText();
                Rua = tfdEnderecoAtendente.getText();
                Bairro = tfdBairroAtendente.getText();
                try {
                    Numero = Integer.parseInt(tfdNumeroAtendente.getText());
                } catch (NumberFormatException nfe) {
                    JOptionPane.showMessageDialog(null, "Formato de Número Inválido");
                }       CEP = tfdCEPAtendente.getText();
                DataContratacao = tfdDataContratacao.getText();
                break;
            case 3:
                Nome = tfdNomeMedico.getText();
                RG =  tfdRGMedico.getText();
                CPF = tfdCPFMedico.getText();
                Nascimento = tfdNascMedico.getText();
                Telefone =tfdTelefoneMedico.getText();
                CRM = tfdCRM.getText();
                Rua = tfdEnderecoMedico.getText();
                Bairro = tfdBairroMedico.getText();
                try {
                    Numero = Integer.parseInt(tfdNumeroMedico.getText());
                } catch (NumberFormatException nfe) {
                    JOptionPane.showMessageDialog(null, "Formato de Número Inválido");
                }       CEP = tfdCEPMedico.getText();
               
                break;
            default:
                break;
        }
      
	        if(Nome.isEmpty()){
	        	CamposInvalidos = true;
                        CamposVazios += "\n"+"Nome"+"\n";
	        }
                if(CPF.isEmpty()){
	        	CamposInvalidos = true;
                        CamposVazios += "CPF"+"\n";
	        }
                if(RG.isEmpty()){
	        	CamposInvalidos = true;
                        CamposVazios += "R.G."+"\n";
	        }
                if(Nascimento.equals("  /  /    ")){
	        	CamposInvalidos = true;
	        	CamposVazios += "Nascimento"+"\n";
	        }
                if(TipoSanguineo.equals("Selecione")&&TIPO==1){
	        	CamposInvalidos = true;
	        	CamposVazios += "TipoSanguineo"+"\n";
	        }
                if(Telefone.equals("(  )      -    -")) {
	        	CamposInvalidos = true;
	        	CamposVazios += "Telefone"+"\n";
	        }
                if(Bairro.isEmpty()) {
	        	CamposVazios += "Bairro"+"\n";
	        	CamposInvalidos = true;
	        }
                if(Rua.isEmpty()) {
	        	CamposInvalidos = true;
	        	CamposVazios += "Rua"+"\n";
	        }
                if(CEP.equals("     -   ")) {
	        	CamposInvalidos = true;
	        	CamposVazios += "CEP"+"\n";
	        }
                if(PlanoSaude.isEmpty() && TIPO==1) {
	        	CamposInvalidos = true;
	        	CamposVazios += "PlanoSaude"+"\n";
	        }
                if(DescricaoPS.isEmpty()&& TIPO==1) {
	        	CamposInvalidos = true;
	        	CamposVazios += "Descricao"+"\n";
	        }
                if(DataContratacao.equals("  /  /    ") && TIPO==2){
                        CamposInvalidos = true;
                        CamposVazios += "Data de Contratação"+"\n";
                }
                
                if(CRM.isEmpty() && TIPO==3 ){
                        CamposInvalidos = true;
                        CamposVazios += "C.R.M"+"\n";
                }		
                
                
                
	     if(CamposInvalidos==true) {
	    	 JOptionPane.showMessageDialog(null, "Os Campos estão Vazio: "+CamposVazios);
	     }else {
	    	Telefone phone;
                phone = new Telefone();
                phone.setTelefone(Telefone);
                phone.setTipo("Celular"); // DEPOIS CONFIGURAR PARA SALVAR OUTROS CAMPOS DE TELEFONE COMO FIXO OU FAZ
        
                Endereco end = new Endereco();
                end.setBairro(Bairro);
                end.setRua(Rua);
                end.setNumero(Integer.toString(Numero));
                end.setCEP(CEP);
        
                PlanoSaude ps = new PlanoSaude();
                ps.setTitulo(PlanoSaude);
                ps.setDescricao(DescricaoPS);
                String strTipoSangue = jcbTipoSanguineos.getSelectedItem().toString();
                
                if(TIPO==1) //TIPO 1 - PACIENTE
                CadastrarPaciente(Nome, RG, CPF, Nascimento, strTipoSangue, Observacao, ps, phone, end);
                else if (TIPO==2) { // TIPO 2 - ATENDENTE
                CadastrarAtendente(Nome, RG, CPF, Nascimento, DataContratacao, phone, end);
                }else if(TIPO==3) { // TIPO 3 - MEDICO
                	Especializacao especial = new Especializacao();
                	
                	
                CadastrarMedico(Nome, RG, CPF, Nascimento, CRM, especial, phone, end);	                	
                }
                	
	     }
    }
   
